const GlobalStyle = createGlobalStyle`
  * {
    margin: 0;
    padding: 0;
    box-sizing: border-box;
  }

  body {
    margin: 0;
    padding: 0;
    overflow-x: hidden;
  }
`;

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const router = useRouter();
  const [isSubscribePopupOpen, setIsSubscribePopupOpen] = useState(false);


  const handleSubscribeClick = () => {
    setIsSubscribePopupOpen(true);
  };

  const handleCloseSubscribe = () => {
    setIsSubscribePopupOpen(false);
  };


  return (
    <html lang="ko">
      
      <body 
      // className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        <StyledComponentsRegistry>
          <GlobalStyle />
          <AuthProvider>
          <CoupangApiProvider>
            <EmailProvider>

            <AppContainer>
              <Header 
                onSubscribeClick={handleSubscribeClick}
                onInquiryClick={() => router.push('/inquiry')}  
              />
              <Main>
                {children}
              </Main>
            <Footer />
            {isSubscribePopupOpen && (
              <SubscribePopup onClose={handleCloseSubscribe} />
            )}
          </AppContainer>
          </EmailProvider>
          </CoupangApiProvider>
          </AuthProvider>
        </StyledComponentsRegistry>
      </body>
    </html>
  );
}

const AppContainer = styled.div`
  min-height: 100vh;
  display: flex;
  flex-direction: column;
  margin: 0;
  padding: 0;
`;

